"""Generate a Markdown benchmark report from pytest-benchmark JSON output.

Usage:
    python benchmarks/generate_report.py benchmark-results.json

Or pipe from pytest:
    pytest benchmarks/ --benchmark-only --benchmark-json=- | python benchmarks/generate_report.py
"""

import json
import sys
import os
from datetime import datetime


def generate_report(data: dict) -> str:
    """Convert pytest-benchmark JSON to a GitHub-flavored Markdown table."""
    benchmarks = data.get("benchmarks", [])
    if not benchmarks:
        return "_No benchmark results available._"

    lines = [
        "# QSCG Performance Benchmark Report",
        "",
        f"Generated: {datetime.utcnow().strftime('%Y-%m-%d %H:%M UTC')}",
        f"Machine: {data.get('machine_info', {}).get('cpu', {}).get('brand_raw', 'Unknown')}",
        f"Python: {data.get('machine_info', {}).get('python_implementation', 'Unknown')} "
        f"{data.get('machine_info', {}).get('python_version', 'Unknown')}",
        "",
        "## Results",
        "",
        "| Benchmark | Mean (ms) | Min (ms) | Max (ms) | Ops/sec |",
        "|:----------|----------:|---------:|---------:|--------:|",
    ]

    for bench in benchmarks:
        name = bench["name"].replace("test_", "").replace("_", " ")
        stats = bench["stats"]
        mean_ms = stats["mean"] * 1000
        min_ms = stats["min"] * 1000
        max_ms = stats["max"] * 1000
        ops = stats.get("ops", 1.0 / stats["mean"]) if stats["mean"] > 0 else 0
        lines.append(
            f"| {name} | {mean_ms:8.2f} | {min_ms:8.2f} | {max_ms:8.2f} | {ops:8.2f} |"
        )

    lines.extend([
        "",
        "## Notes",
        "",
        "- `Mean`: average time across all measured rounds",
        "- `Ops/sec`: approximate throughput (higher is better)",
        "- ML-DSA and ML-KEM are pure-Python educational implementations; ",
        "  production deployments should use optimized C/Rust backends",
        "",
        "---",
        "",
        "*Report generated by `benchmarks/generate_report.py`*",
    ])

    return "\n".join(lines)


def main() -> None:
    if len(sys.argv) < 2:
        print("Usage: python generate_report.py <benchmark-results.json>")
        sys.exit(1)

    path = sys.argv[1]
    if path == "-":
        data = json.load(sys.stdin)
    else:
        with open(path) as f:
            data = json.load(f)

    report = generate_report(data)
    print(report)

    # Also write to docs/benchmark-report.md for Pages
    output_path = os.path.join(
        os.path.dirname(__file__), "..", "docs", "benchmark-report.md"
    )
    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    with open(output_path, "w") as f:
        f.write(report)
    print(f"\nReport also written to: {output_path}", file=sys.stderr)


if __name__ == "__main__":
    main()
